const Card = ({ seriesName, match }) => {
    const info = match.matchInfo;
    const score = match.matchScore;

    const team1 = info.team1;
    const team2 = info.team2;

    const team1Name = `${team1.teamName} (${team1.teamSName})`;
    const team2Name = `${team2.teamName} (${team2.teamSName})`;

    const desc = info.matchDesc;
    const format = info.matchFormat;
    const status = info.status;

    const venue = `${info.venueInfo.ground}, ${info.venueInfo.city}`;
    const matchDate = new Date(Number(info.startDate)).toLocaleDateString("en-GB", {
        day: "numeric", month: "short", year: "numeric"
    });

    const team1Runs = score?.team1Score?.inngs1?.runs ?? "-";
    const team1Wkts = score?.team1Score?.inngs1?.wickets ?? "-";

    const team2Runs = score?.team2Score?.inngs1?.runs ?? "-";
    const team2Wkts = score?.team2Score?.inngs1?.wickets ?? "-";

    return (
        <div className="card">
            <h3>{seriesName}</h3>
            <p><strong>{desc}</strong> — {format}</p>
            <p>Date: {matchDate}</p>
            <p>{team1Name} vs {team2Name}</p>
            <p>Venue: {venue}</p>
            <p>Score:</p>
            <ul>
                <li>{team1.teamSName}: {team1Runs}/{team1Wkts}</li>
                <li>{team2.teamSName}: {team2Runs}/{team2Wkts}</li>
            </ul>
            <p>Status: {status}</p>
        </div>
    );
};


const App = () => {
    const allMatches = [];

    typeMatches.forEach((type) => {
        type.seriesMatches.forEach((series) => {
            const wrapper = series.seriesAdWrapper;
            if (wrapper && wrapper.matches) {
                wrapper.matches.forEach((match) => {
                    allMatches.push({ seriesName: wrapper.seriesName, match });
                });
            }
        });
    });

    return (
        <div className="parent">
            {allMatches.map((item, index) => (
                <Card key={index} seriesName={item.seriesName} match={item.match} />
            ))}
        </div>
    );
};

reactRoot.render(<App />);
